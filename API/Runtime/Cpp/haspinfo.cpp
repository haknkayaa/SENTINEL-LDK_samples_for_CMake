#include "hasp_api_cpp_.h"


////////////////////////////////////////////////////////////////////
// Construction/Destruction
////////////////////////////////////////////////////////////////////

ChaspInfo::ChaspInfo()
    : m_pszInfo(NULL)
{
}

ChaspInfo::~ChaspInfo()
{
    clear();
}

////////////////////////////////////////////////////////////////////
// Implementation
////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////
ChaspInfo::operator const char *() const
{
    return m_pszInfo;
}

////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////
void ChaspInfo::clear()
{
    if (NULL != m_pszInfo)
    {
        hasp_free(m_pszInfo);
        m_pszInfo = NULL;
    }
}

////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////
const char* ChaspInfo::getInfo() const
{
    return m_pszInfo;
}

